valor1=int(input("Insira o primeiro valor a ser comparado: "))
valor2=int(input("Insira o segundo valor a ser comparado: "))

if valor1 > valor2:
    print("O primeiro valor é maior do que o segundo")
elif valor1 == valor2:
    print("Os valores são identicos")
else:
    print("O segundo valor é maior do que o primeiro")